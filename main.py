# 6. Hitung deltaF
data_combined['Fcal'] = np.sqrt(data_combined['SRGX']**2 + data_combined['SRGY']**2 + data_combined['SRGZ']**2)
data_combined['deltaF'] = data_combined['Fcal'] - data_combined['SRGF']
# 7. Ambil semua tanggal unik
unique_dates = sorted(data_combined['DATE'].unique())

