import matplotlib.pyplot as plt
import matplotlib.dates as mdates
import pandas as pd

# Siapkan list tanggal dan deltaF_range
deltaF_ranges = []
tanggal_list = []

for date in unique_dates:
    daily_data = data_combined[data_combined['DATE'] == date]
    deltaF_range = daily_data['deltaF'].max() - daily_data['deltaF'].min()
    deltaF_ranges.append(deltaF_range)
    tanggal_list.append(pd.to_datetime(date))  # pastikan format datetime

# Warna: hijau jika deltaF < 10, merah jika >= 10
colors = ['green' if val < 10 else 'red' for val in deltaF_ranges]

# Plot scatter
plt.figure(figsize=(15, 6))
plt.scatter(tanggal_list, deltaF_ranges, c=colors, s=30, alpha=0.7, edgecolors='k')

# Garis ambang kualitas
plt.axhline(10, color='blue', linestyle='--', linewidth=1, label='Ambang kualitas (10 nT)')

# Format sumbu X per bulan
plt.gca().xaxis.set_major_locator(mdates.MonthLocator())  # penanda tiap bulan
plt.gca().xaxis.set_major_formatter(mdates.DateFormatter('%b %Y'))  # format: Jan 2023, Feb 2023, dst

plt.xticks(rotation=45)  # miringkan label agar tidak saling tumpuk

# Label dan tampilan
plt.title('Scatter Plot Rentang deltaF Harian (max - min)')
plt.xlabel('Tanggal')
plt.ylabel('Rentang deltaF (nT)')
plt.grid(True)
plt.legend()
plt.tight_layout()
plt.show()