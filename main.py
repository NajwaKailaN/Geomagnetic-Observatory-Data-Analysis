os.makedirs(output_path, exist_ok=True)

# Parameter validasi
jumlah_data_per_hari = 86400   # 24 jam * 60 menit * 60 detik
ambang_valid = 0.9             # 90% valid

# Fungsi mengecek apakah satu baris data valid
def is_valid_line(line):
    try:
        parts = line.strip().split()
        if len(parts) < 5:
            return False
        x = float(parts[2])
        y = float(parts[3])
        z = float(parts[4])
        if x == 99999.00 or y == 99999.00 or z == 99999.00:
            return False
        return True
    except:
        return False

# Loop dari bulan 01 sampai 12
for bulan in range(1, 13):
    bulan_folder = f"{bulan:02d}"  # Format '01', '02', ..., '12'
    folder_path = os.path.join(base_path, bulan_folder, 'IAGA', 'vsec')

    if os.path.exists(folder_path):
        print(f"\n📁 Membaca data dari: {folder_path}")
        for filename in sorted(os.listdir(folder_path)):
            file_path = os.path.join(folder_path, filename)
            if os.path.isfile(file_path) and filename.endswith(".sec"):
                with open(file_path, 'r') as file:
                    lines = file.readlines()

                data_lines = [line for line in lines if line[:1].isdigit()]
                valid_lines = [line for line in data_lines if is_valid_line(line)]
                persen_valid = len(valid_lines) / jumlah_data_per_hari

                if persen_valid >= ambang_valid:
                    nama_baru = os.path.splitext(filename)[0] + "_Valid.sec"
                    output_file = os.path.join(output_path, nama_baru)

                    with open(output_file, 'w') as f_out:
                        f_out.writelines(lines)

                    print(f"\n✔ File valid → {filename}")
                    print(f"  - Total baris data: {len(data_lines)}")
                    print(f"  - Baris valid     : {len(valid_lines)}")
                    print(f"  - Persen valid    : {persen_valid:.2%}")
                    print(f"  - Disimpan sebagai: {nama_baru}")
                else:
                    print(f"✘ File tidak valid → {filename} ({persen_valid:.2%})")
    else:
        print(f"⚠ Folder tidak ditemukan: {folder_path}")

