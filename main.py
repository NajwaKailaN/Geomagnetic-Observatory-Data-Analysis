all_data = []

# Loop semua file di folder
for filename in os.listdir(folder_path):
    # Cek ekstensi .sec dan nama file tidak mengandung '(1)'
    if filename.endswith('.sec') and '(1)' not in filename:
        filepath = os.path.join(folder_path, filename)
        # Baca file, skip 12 baris pertama, tanpa header
        df = pd.read_csv(filepath, sep='\s+', skiprows=13, header=None)

        # Beri nama kolom ulang
        df.columns = ['DATE', 'TIME', 'DOY', 'SRGX', 'SRGY', 'SRGZ', 'SRGF']

        # Simpan ke list
        all_data.append(df)

# Gabungkan semua data dari semua file menjadi satu DataFrame besar (opsional)
data_combined = pd.concat(all_data, ignore_index=True)

# Drop kolom 'DOY'
data_combined = data_combined.drop(columns=['DOY'])

# Drop baris yang mengandung 99999 pada kolom sensor
sensor_columns = ['SRGX', 'SRGY', 'SRGZ', 'SRGF']
data_combined = data_combined[
    ~(data_combined[sensor_columns] == 99999).any(axis=1)
]

